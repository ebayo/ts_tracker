import numpy as np

# Auxiliar functions

def xyxy2xywh(x):
    # from yolov5
    y = np.ndarray((x.shape[0], 4))
    y[:, 0] = (x[:, 0] + x[:, 2]) / 2  # x center
    y[:, 1] = (x[:, 1] + x[:, 3]) / 2  # y center
    y[:, 2] = x[:, 2] - x[:, 0]  # width
    y[:, 3] = x[:, 3] - x[:, 1]  # height
    return y
